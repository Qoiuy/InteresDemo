package Lilac.bean;

public class User {

		private String username;
		private String password;
		private String remember_user;
		private String remember_login;
		public String getUsername() {
			return username;
		}
		public void setUsername(String username) {
			this.username = username;
		}
		public String getPassword() {
			return password;
		}
		public void setPassword(String password) {
			this.password = password;
		}
		public String getRemember_user() {
			return remember_user;
		}
		public void setRemember_user(String remember_user) {
			this.remember_user = remember_user;
		}
		public String getRemember_login() {
			return remember_login;
		}
		public void setRemember_login(String remember_login) {
			this.remember_login = remember_login;
		}
		
}
